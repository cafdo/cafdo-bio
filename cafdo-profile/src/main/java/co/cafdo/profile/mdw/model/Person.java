package co.cafdo.profile.mdw.model;

public class Person {
	private String person;
	private String id;
	private String publicId;
	private String name;
	private String email;
	private Boolean hasEmail;
	private Boolean showEmail;
	private String professionalHeadline;
	private String location;
	private String picture;
	private String theme;
	private String phone;
	private Boolean showPhone;
	private String weight;
	private String completion;
	private Boolean verified;
	private Boolean claimant;
	public String getPerson() {
		return person;
	}
	public void setPerson(String person) {
		this.person = person;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPublicId() {
		return publicId;
	}
	public void setPublicId(String publicId) {
		this.publicId = publicId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public Boolean getHasEmail() {
		return hasEmail;
	}
	public void setHasEmail(Boolean hasEmail) {
		this.hasEmail = hasEmail;
	}
	public Boolean getShowEmail() {
		return showEmail;
	}
	public void setShowEmail(Boolean showEmail) {
		this.showEmail = showEmail;
	}
	public String getProfessionalHeadline() {
		return professionalHeadline;
	}
	public void setProfessionalHeadline(String professionalHeadline) {
		this.professionalHeadline = professionalHeadline;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
	public String getTheme() {
		return theme;
	}
	public void setTheme(String theme) {
		this.theme = theme;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public Boolean getShowPhone() {
		return showPhone;
	}
	public void setShowPhone(Boolean showPhone) {
		this.showPhone = showPhone;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getCompletion() {
		return completion;
	}
	public void setCompletion(String completion) {
		this.completion = completion;
	}
	public Boolean getVerified() {
		return verified;
	}
	public void setVerified(Boolean verified) {
		this.verified = verified;
	}
	public Boolean getClaimant() {
		return claimant;
	}
	public void setClaimant(Boolean claimant) {
		this.claimant = claimant;
	}
}
